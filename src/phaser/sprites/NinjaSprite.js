/**
 * Generate a Phaser CanvasTexture from a 16x16 pixel grid.
 *
 * @param {Phaser.Scene} scene - Active scene for texture creation
 * @param {string} key - Texture key (e.g. 'ninja-idle')
 * @param {number[][]} grid - 16x16 array of palette indices
 */
function paintTexture(scene, key, grid) {
    if (scene.textures.exists(key)) return key

    const canvas = scene.textures.createCanvas(key, 16, 16)
    const ctx = canvas.getContext()

    for (let y = 0; y < 16; y++) {
        for (let x = 0; x < 16; x++) {
            const pixel = grid[y][x]
            if (pixel === 0) continue
            ctx.fillStyle = PALETTE[pixel]
            ctx.fillRect(x, y, 1, 1)
        }
    }

    canvas.refresh()

    // Force NEAREST filtering so pixel art stays crisp when scaled up
    scene.textures.get(key).setFilter(Phaser.Textures.FilterMode.NEAREST)

    return key
}

/**
 * Create all ninja pose textures in the given scene.
 * Call once in scene.create().
 */
export function createNinjaTextures(scene) {
    Object.entries(POSES).forEach(([pose, grid]) => {
        paintTexture(scene, `ninja-${pose}`, grid)
    })
}

